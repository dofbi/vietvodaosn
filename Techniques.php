<p align="center"><strong><u>LES SONG LUYENS</u></strong></p>
<p>Sons luyens signifie combat codifi&eacute;  entre &nbsp;deux adversaires. Ils existent des  song luyens &agrave; mains nues ou avec arm&eacute;es. Lors des d&eacute;monstrations, les song  luyen libre sont autoris&eacute;s, laissant donc aux vo-shing une certaine part de  cr&eacute;ativit&eacute;. Tout est autoris&eacute; dans ce genre d&rsquo;exercice, les coups saut&eacute;s, les  ciseaux, la lutte, les acrobaties, les combats &agrave; deux ou trois contre un etc&hellip;  Il est donc bien &eacute;vident que le song luyen est le meilleur moyen pour montrer  la richesse de notre art.</p>
<p>&nbsp;</p>
<p align="center"><strong><u>LES VAT</u></strong><br />
Les vat sont assimilables &agrave; des  techniques de judo-jujitsu. A l&rsquo;origine, ces combats de corps &agrave; corps se  d&eacute;roulaient le torse nu et huil&eacute; afin d&rsquo;&eacute;chapper aux saisies de &nbsp;l&rsquo;adversaire.</p>
<p align="center">&nbsp;</p>
<p align="center"><strong><u>LES ARMES</u></strong><br />
Le Vovinam Vi&ecirc;t Vo Dao est &agrave; la base  une m&eacute;thode de combat guerri&egrave;re, on ne peut donc omettre la pr&eacute;sence d&rsquo;armes  qui peuvent s&rsquo;av&eacute;rer tr&egrave;s efficaces sur le champs de bataille.</p>
<p>Les paysans vietnamiens ont d&ucirc;t pour  se d&eacute;fendre de l&rsquo;envahisseur se servir des objets &agrave; porter de leur mains afin  de se prot&eacute;ger. On retrouve donc au Vovinam des armes traditionnelles port&eacute;es  en g&eacute;n&eacute;ral par les nobles comme le sable ou des armes typiquement paysanne  comme le ma tao (machette) ou le bong (baton).</p>
<p>Ses techniques d&rsquo;armes sont &eacute;tudi&eacute;es  de la m&ecirc;me mani&egrave;re que les techniques mains nues. Tout d&rsquo;abord, on apprend leur  maniement, la mani&egrave;re de se d&eacute;fendre avec puis on assemble ses gestes pour les  ins&eacute;rer dans un quyen et enfin, on les applique face &agrave; face dans un song luyen.<br />
  La travaille des armes n&eacute;cessite une  bonne base technique et un s&eacute;rieux total afin d&rsquo;&eacute;viter les blessures. <br />
  Ils existent plusieurs type d&rsquo;arme au  Vovinam Vi&ecirc; V&ocirc; Dao.<br />
  Arme de courte distance&nbsp;:  Couteau, double couteau, r&egrave;gle plat, &eacute;ventail, Hache, etc&hellip;<br />
  Arme de moyenne distance&nbsp;:  Sabre, simeterre, machette, baton court etc&hellip;<br />
  Arme de longue distance&nbsp;: Baton  long, hallebarde etc&hellip; </p>
