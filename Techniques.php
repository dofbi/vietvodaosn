<p align="center"><strong><u>LES TECHNIQUES DE BASE</u></strong></p>
<p>Les techniques de base sont pr&eacute;sentes  dans les arts martiaux. Elles comprennent les frappes avec le membre sup&eacute;rieur,  le membre inf&eacute;rieur et les positons. A travers ces techniques, le Vo-shin  (Pratiquant de Vovinam Vi&ecirc;t Vo Dao) se construira les bases n&eacute;cessaires &agrave; la  suite de son apprentissage. Le point fort du Vovinam Viet Vo Dao r&eacute;side dans  ces techniques qui seront &eacute;tudi&eacute;es de mani&egrave;res similaires afin de ne pas  privil&eacute;gier les jambes sur les poing ou l&rsquo;inverse. Notre art martial rec&egrave;le six  grandes cat&eacute;gories de technique de base r&eacute;pertori&eacute;es dans trois familles.</p>
