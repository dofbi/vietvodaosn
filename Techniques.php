<p align="center"><strong><u>LES QUYENS</u></strong></p>
<p>Les quyens repr&eacute;sentent toute  l&rsquo;essence de l&rsquo;art martial. En effet, c&rsquo;est &agrave; travers eux que le symbole du  bambou se manifeste. Les quyens se situent entre les katas du karat&eacute; et les  taos di kungfu. Le karat&eacute; est assez secs avec des positions tr&egrave;s basse et des  gestes tr&egrave;s d&eacute;coup&eacute;. Le kungfu est surtout ax&eacute; sur les sauts, les acrobaties  a&eacute;riennes, les coups pieds &agrave; rotation verticale et un mim&eacute;tisme &nbsp;tr&egrave;s prononc&eacute; des animaux. Le Vovinam  rassemble ses deux aspects du comportement pour cr&eacute;er les quyens qui sont secs  dans certaines phases et souples dans l&rsquo;autres. Contrairement au kungfu, Ma&icirc;tre  Nguyen Loc m&ecirc;me si il a utilis&eacute; les animaux dans la mise en place de certains  quyens, c&rsquo;est arrang&eacute; pour que seul le pratiquant soit capable de les rep&eacute;rer.  Ceci donne peut &ecirc;tre au quyens plus de cr&eacute;dibilit&eacute; (ceci est une question de  point de vue).</p>
