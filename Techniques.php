<p align="center"><strong><u>LES KHOA GO TU V&ecirc;</u></strong></p>
<p>Les khoa go tu v&ecirc; (self  d&eacute;fenses) sont des techniques permettant d&rsquo;&eacute;chapper aux saisies d&rsquo;une  adversaires (aussi fort soit il). En effet, il est devrai que dans les arts  martiaux et dans la salle d&rsquo;entra&icirc;nement se soient doivent les poings et les  pieds qui sont utilis&eacute;s pour attaquer, mais une fois dehors qui sait comment  votre adversaire va r&eacute;agir&nbsp;? Tr&egrave;s souvent, lors d&rsquo;une agression, ce sont  les saisies qui priment et non les coups. C&rsquo;est donc pour que le Vovinam Vi&ecirc;t  Vo Dao a codifi&eacute; 30 techniques de selfs d&eacute;fenses r&eacute;parties en trois niveaux.  Les selfs d&eacute;fenses permettent non seulement d&rsquo;&eacute;chapper &agrave; la saisie mais aussi  de renverser l&rsquo;adversaire par des techniques de projections, balayages et  immobilisation.<br />
  Le but premier des khoa  go tu v&ecirc; est de se d&eacute;faire de la saisie de l&rsquo;adversaire afin de pouvoir ensuite  l&rsquo;attaque.<br />
  &nbsp;Ces techniques ne s&rsquo;effectuent pas en force  (au contraire). &nbsp;</p>
