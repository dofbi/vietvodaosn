<font face="police"size="4"color="#FFFFFF">Les disciples du Maître Fondateur Nguyen Loc étaient nombreux, mais pour des raisons diverses, personne n’était aussi attaché à la carrière du Maître Fondateur que Maître Lê Sang, Maître Patriarche actuel. 
Maître Le Sang, qui naquit en 1920 à Ha Noi devint disciple du Maître Fondateur en 1940. Il ne fut pas seulement quelqu’un ayant travaillé aux cotés du Maître Fondateur mais également un membre de sa famille, ayant vécu avec lui comme un proche parent. C’est pour ces raisons que l’art martial et la philosophie de l’art martial du Maître Le Sang a été pour ainsi dire intégralement retransmis des mains du Maître Fondateur Nguyen Loc. 
Vers la fin de l’année 1957, le Maître Fondateur Nguyen Loc conféra au Maître Le Sang le droit d’enseigner la génération des disciples de 1954. 
Et avant son décès (1960), il confia au Maître Lê Sang la mission de diriger l’école VOVINAM Viêt Vo Dao.
Il réside actuellement au TO DUONG.</font> <font color="#FFFFFF"><br>
